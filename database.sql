-- 1. PROFILES TABLE (Linked to auth.users)
-- Stores user-specific metadata
CREATE TABLE IF NOT EXISTS public.profiles (
    id UUID REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
    full_name TEXT,
    avatar_url TEXT,
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. DRESSES TABLE (Active Lines & Product Editor Data)
-- Stores the JSON structure for garment production planning
CREATE TABLE IF NOT EXISTS public.dresses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL DEFAULT auth.uid() REFERENCES auth.users(id) ON DELETE CASCADE,
    content JSONB NOT NULL DEFAULT '{}'::jsonb,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- 3. PORTFOLIO_HUB TABLE
-- Stores specific portfolio notes, titles, and original texts
CREATE TABLE IF NOT EXISTS public.portfolio_hub (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL DEFAULT auth.uid() REFERENCES auth.users(id) ON DELETE CASCADE,
    title TEXT NOT NULL,
    original_text TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- 4. ENABLE ROW LEVEL SECURITY (RLS)
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.dresses ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.portfolio_hub ENABLE ROW LEVEL SECURITY;

-- 5. RLS POLICIES
-- Profiles: Users can only view and edit their own profile
CREATE POLICY "Users can manage their own profile" 
ON public.profiles FOR ALL TO authenticated 
USING (auth.uid() = id) 
WITH CHECK (auth.uid() = id);

-- Dresses: Users can only manage their own production lines
CREATE POLICY "Users can manage their own dresses" 
ON public.dresses FOR ALL TO authenticated 
USING (auth.uid() = user_id) 
WITH CHECK (auth.uid() = user_id);

-- Portfolio Hub: Users can only manage their own portfolio entries
CREATE POLICY "Users can manage their own portfolio_hub" 
ON public.portfolio_hub FOR ALL TO authenticated 
USING (auth.uid() = user_id) 
WITH CHECK (auth.uid() = user_id);

-- 6. AUTOMATIC PROFILE CREATION
-- Function to create a profile entry whenever a new user signs up
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.profiles (id, full_name, avatar_url)
  VALUES (
    new.id, 
    new.raw_user_meta_data->>'full_name', 
    new.raw_user_meta_data->>'avatar_url'
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Trigger to execute the function on signup
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- 7. INDEXES FOR PERFORMANCE
CREATE INDEX IF NOT EXISTS idx_dresses_user_id ON public.dresses(user_id);
CREATE INDEX IF NOT EXISTS idx_portfolio_hub_user_id ON public.portfolio_hub(user_id);
